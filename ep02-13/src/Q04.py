from automata.tm.mntm import MNTM


mntm4 = MNTM(
    states={'start', 'done', 'q1', 'q2', 'q3', 'q4', 'q5'},
    input_symbols={'a', 'b', '#'},
    tape_symbols={'a', 'b', '#', '.'},
    n_tapes=2,
    transitions= {
        'start': {
          ('a', '.'): [('q1', (('a', 'N'), ('#', 'R')))],
          ('b', '.'): [('q1', (('b', 'N'), ('#', 'R')))],
          ('#', '.'): [('q1', (('#', 'N'), ('#', 'R')))]
        },
        'q1': {
          ('a', '.'): [('q1', (('a', 'R'), ('a', 'R')))],
          ('b', '.'): [('q1', (('b', 'R'), ('b', 'R')))],
          ('#', '.'): [('q2', (('#', 'R'), ('#', 'L')))]
        },
        'q2': {
          ('a', 'a'): [('q2', (('a', 'N'), ('a', 'L')))],
          ('b', 'b'): [('q2', (('b', 'N'), ('b', 'L')))],
          ('a', 'b'): [('q2', (('a', 'N'), ('b', 'L')))],
          ('b', 'a'): [('q2', (('b', 'N'), ('a', 'L')))],
          ('a', '#'): [('q3', (('a', 'N'), ('#', 'R')))],
          ('b', '#'): [('q3', (('b', 'N'), ('#', 'R')))],
          ('.', '#'): [('done', (('b', 'N'), ('#', 'R')))]
          
        },
        'q3':{
          ('a', 'a'): [('q3', (('a', 'R'), ('a', 'R')))],
          ('b', 'b'): [('q3', (('b', 'R'), ('b', 'R')))],
          ('a', 'b'): [('q4', (('a', 'N'), ('b', 'N')))],
          ('b', 'a'): [('q4', (('b', 'N'), ('a', 'N')))],
          ('a', '#'): [('done', (('a', 'R'), ('#', 'R')))],
          ('b', '#'): [('done', (('b', 'R'), ('#', 'R')))],
          ('.', '#'): [('done', (('.', 'N'), ('#', 'N')))]
        },
        'q4':{
          ('a', 'a'): [('q4', (('a', 'N'), ('a', 'L')))],
          ('b', 'b'): [('q4', (('b', 'N'), ('b', 'L')))],
          ('a', 'b'): [('q4', (('a', 'N'), ('b', 'L')))],
          ('b', 'a'): [('q4', (('b', 'N'), ('a', 'L')))],
          ('a', '#'): [('q5', (('a', 'N'), ('#', 'R')))],
          ('b', '#'): [('q5', (('b', 'N'), ('#', 'R')))]
        },
        'q5':{
          ('a', 'a'): [('q3', (('a', 'N'), ('a', 'N')))],
          ('b', 'b'): [('q3', (('b', 'N'), ('b', 'N')))],
          ('a', 'b'): [('q3', (('a', 'R'), ('b', 'N')))],
          ('b', 'a'): [('q3', (('b', 'R'), ('a', 'N')))]
        }
    },
    initial_state='start',
    blank_symbol='.',
    final_states={'done'},
)
